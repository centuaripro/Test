
names = ["Mike", "Jane", "Blessing", "Peter", "Joe", "Sarah", "Chris", "Daniel", "Jasper", "Irine"]
print(len(names))

print("6th item:", names[5])
print("last item:", names[9])

#You can retrieve the last item using negative index
print("last item using negative index:", names[-1])

#alternatively you can use the example below
index = len(names) - 1
print(names[index])
