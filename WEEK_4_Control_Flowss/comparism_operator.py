
# comparism operators are symbols we use in python to compare two valuesclear

# one "equal to" symbol means assignment
# Double "equal to" symbol is for comparism

print("Is 5 equal to 5?", 5 ==5)

# Not Equal to
print("is 5 != 5", 5 != 5)

# Greater than (>)
print("is 7 greater than 10", 7 > 10)

# less than (<)
print("is 7 less than 10", 7 < 10)

# Greater than or Equal to(>=)
print("is 7 greater or equal to 10", 7 >= 10)

# less than or Equal to(<=)
print("is 7 less than or equal to 10", 7 <= 10)
